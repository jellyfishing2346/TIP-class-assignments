def greeting(name):
    print("Welcome to The Hundred Acre Wood " + name + "! My name is Christopher Robin.")

greeting("Michael")  # Output: Welcome to The Hundred Acre Wood Michael! My name is Christopher Robin.
greeting("Winnie the Pooh")  # Output: Welcome to The Hundred Acre Wood Winnie the Pooh! My name is Christopher Robin.
greeting("Piglet")  # Output: Welcome to The Hundred Acre Wood Piglet! My name is Christopher Robin.
greeting("Eeyore")  # Output: Welcome to The Hundred Acre Wood Eeyore! My name is Christopher Robin.
greeting("Tigger")  # Output: Welcome to The Hundred Acre Wood Tigger! My name is Christopher Robin.    